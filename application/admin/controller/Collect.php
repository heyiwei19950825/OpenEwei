    /**
     * 保存数据
     */
    public function saveData(){

        if( $this->request->isPost() ){
            $params  = $this->request->param();
            $params['data']    = json_decode($params['data'],true);
            $config  = Helper::convertUrlArray($params['config']);

            //独立数据转换
            if($params['alone'] == true){
                $data[] = $params['data'];
            }else{
                $data = $params['data'];
            }


            if(empty($data) ){
                return $this->error('暂无数据');
            }

            //检测表是否存在
            $checkRow = Db::query('show tables like "oc_'.$config['tableName'].'"');

            //表不存在并且不创建新表 提示错误
            if( empty($checkRow) ){

                return $this->error('数据表不存在');
            }
            try{
                //创建表
                if( $config['field'] != 0 ){
                    $field = array_keys($data[0]);
                    $tableField = Db::query('desc oc_'.$config['tableName']);
                    $tableFields = [];
                    foreach($tableField as $k=>$v){
                        $tableFields[] = $v['Field'];
                    }
                    $sql = '';

                    //检测字段是否已经存在表中   如果没有则生成sql语句              //生成sql语句
                    foreach ($field as $k=>$v){
                        if(!in_array($v,$tableFields)){
                            $len = ''; // 字段类型
                            $demo  = $data[0][$v];

                            if( strlen( $demo ) < 50 ){
                                $len = 'varchar(50)';
                            }
                            switch (strlen( $demo )){
                                case $demo < 50:
                                    $len ='varchar(50)';
                                    break;

                                case $demo < 255:
                                    $len ='varchar(255)';
                                    break;

                                case $demo > 255:
                                    $len ='text';
                                    break;
                            }

                            $sql .= ' ADD COLUMN `'.$v.'` '.$len.' DEFAULT NULL, ';
                        }

                    }
                    //执行sql语句
                    $sql = 'ALTER TABLE `oc_'.$config['tableName'].'`'.rtrim($sql,', ').';';
                    Db::query($sql);
                }
                //数据写入表中
                $insertRow = Db::name($config['tableName'])->insertAll($data);

                if($insertRow){
                    return $this->success('写入成功');
                }

            }catch (Exception $e){
                echo $e->getMessage();die;
                return $this->error('错误数据，请检查2');

            }
        }
    }

